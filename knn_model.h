// KNN推論関数の宣言
int knn_predict(const float* features)
{
    // 1) トレーニングセットとの距離を計算
    float distances[NUM_TRAINING_SAMPLES];
    for (int i = 0; i < NUM_TRAINING_SAMPLES; i++) {
        float sumSq = 0.0f;
        for (int j = 0; j < NUM_FEATURES; j++) {
            float diff = training_features[i][j] - features[j];
            sumSq += diff * diff;
        }
        distances[i] = sqrtf(sumSq);
    }

    // 2) 距離が小さい順に上位K_VALUE件を選ぶ
    int   neighborIndices[K_VALUE];
    float neighborDist[K_VALUE];
    for (int k = 0; k < K_VALUE; k++) {
        neighborIndices[k] = -1;
        neighborDist[k] = 1e9; // 十分大きい初期値
    }
    for (int i = 0; i < NUM_TRAINING_SAMPLES; i++) {
        float d = distances[i];
        // 上位Kの中でより大きい距離があれば置き換え
        for (int k = 0; k < K_VALUE; k++) {
            if (d < neighborDist[k]) {
                // 後ろをずらす
                for (int m = K_VALUE - 1; m > k; m--) {
                    neighborDist[m] = neighborDist[m - 1];
                    neighborIndices[m] = neighborIndices[m - 1];
                }
                neighborDist[k] = d;
                neighborIndices[k] = i;
                break;
            }
        }
    }

    // 3) 上位K件のクラスラベルを多数決
    // （クラス数が 0,1,2,3 など最大4種程度なら votes[10] で十分）
    int votes[10] = {0};
    for (int k = 0; k < K_VALUE; k++) {
        int lbl = training_labels[neighborIndices[k]];
        votes[lbl]++;
    }

    // 4) 最も票数が多いラベルを返す
    int bestLabel = 0;
    int bestVotes = votes[0];
    for (int i = 1; i < 10; i++) {
        if (votes[i] > bestVotes) {
            bestVotes = votes[i];
            bestLabel = i;
        }
    }
    return bestLabel;
}

